      private void configurarTabla() {
        modeloTabla = (DefaultTableModel) jtProducto.getModel();
        jtProducto.setAutoCreateRowSorter(true);
    }
    
    private void limpiarTabla() {
        modeloTabla.setRowCount(0);
    }
    
    private void listarProductos() throws SQLException {
        limpiarTabla();
        List<Producto> lista = ctrlProducto.listarProductos();

        for (Producto p : lista) {

            modeloTabla.addRow(new Object[]{
                p.getCodigo(),
                p.getNombre(),
                p.getDescripcion(),
                p.getCategoria().getNombre(),
                p.getMarca(),
                p.getPrecioCompra(),
                p.getPrecioVenta(),
                p.getMargenGanancia(),
                p.getStockActual(),
                p.getStockMinimo(),
                p.getStockMaximo()
            });
        }
    }
    
    private boolean validarCampos() {
        if (txtCodigo.getText().trim().isEmpty()
                || txtNombre.getText().trim().isEmpty()
                || txtMarca.getText().trim().isEmpty()
                || txtMargen.getText().trim().isEmpty()
                || txtStockMinimo.getText().trim().isEmpty()
                || txtStockMaximo.getText().trim().isEmpty()
                || cboTipo.getSelectedIndex() == -1) {

            JOptionPane.showMessageDialog(this,
                    "Complete todos los campos obligatorios.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }

        // 2. Validar tipos numéricos: margen, stockMin, stockMax
        try {
            Double.parseDouble(txtMargen.getText().trim()); 
            Integer.parseInt(txtStockMinimo.getText().trim()); 
            Integer.parseInt(txtStockMaximo.getText().trim()); 

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Margen debe ser decimal y los stocks mínimo y máximo deben ser enteros.",
                    "Validación",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
        }

        private void cargarProductos() {
            try {
                listarProductos();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
